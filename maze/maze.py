from typing import List

INV_MOVE = {(1, 0): "S", (-1, 0): "N", (0, -1): "W", (0, 1): "E"}


def find_path(maze_map: List[List[int]]) -> str:
    D = {(1, 1): None}
    q = [(1, 1)]
    maze_map[1][1] = -1
    finish = (10, 10)

    class FoundExitEx(Exception):
        pass

    while True:
        try:
            i, j = q.pop(0)
            for a, b in zip([0, 0, 1, -1], [1, -1, 0, 0]):
                x, y = i + a, j + b
                if maze_map[x][y] == 0:
                    q.append((x, y))
                    D[(x, y)] = (i, j)
                    maze_map[x][y] = -1
                    if (x, y) == finish:
                        raise FoundExitEx()
        except FoundExitEx:
            break

    path = ''
    curr = finish
    while True:
        prev = D[curr]
        if not prev:
            return path
        move = (curr[0] - prev[0], curr[1] - prev[1])
        path = INV_MOVE[move] + path
        curr = prev


def check_path(func, labyrinth):
    moves = {"S": (1, 0), "N": (-1, 0), "W": (0, -1), "E": (0, 1)}
    route = func([row[:] for row in labyrinth])
    pos = (1, 1)
    goal = (10, 10)
    for i, d in enumerate(route):
        move = moves.get(d, None)
        if not move:
            print("Wrong symbol in path")
            return False
        pos = pos[0] + move[0], pos[1] + move[1]
        if pos == goal:
            return True
        if labyrinth[pos[0]][pos[1]] == 1:
            print("Player in the pit")
            return False
    print("Player could not reach finish")
    return False


